    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
        int dk = JOptionPane.showConfirmDialog(this,"Bạn có muốn đăng kí tài khoản không?","Confirm",JOptionPane.YES_NO_OPTION);
        if(dk != JOptionPane.YES_OPTION){
            return;
        }
        try {
            Class.forName(driver);
            Connection conn = (Connection) DriverManager.getConnection(url);
            String sql = "insert into Account values (?,?,?,?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1,txtAccount.getText());
            ps.setString(2,txtEmail.getText());
            ps.setString(3,txtPass.getText());
            ps.setString(4,txtConPass.getText());
            
            int n = ps.executeUpdate();
            if (txtAccount.getText().equals("") || txtEmail.getText().equals("") || txtPass.getText().equals("") ||txtConPass.getText().equals("")){  
                JOptionPane.showMessageDialog(this,"Không thể để thông tin trống");      
            }else if(!txtAccount.getText().equals("") && !txtEmail.getText().equals("") && !txtPass.getText().equals("") && !txtConPass.getText().equals("")) {
                if(txtPass.getText().equals("") != txtConPass.getText().equals("")){
                   JOptionPane.showMessageDialog(this,"Mật khẩu không khớp"); 
                }else {
                    if(n != 0){
                        JOptionPane.showMessageDialog(this,"Đăng kí thành công!");
                   }else {
                        JOptionPane.showMessageDialog(this,"Đăng kí thất bại!");
                    }
                }
                
            }
           
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

